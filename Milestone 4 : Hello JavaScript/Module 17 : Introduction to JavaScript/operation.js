var applePrice = 150;
var bananaPrice = 10;
var pomgranatePrice = 400;

// Addition
var add = applePrice + bananaPrice + pomgranatePrice;
console.log(applePrice);
console.log(applePrice + bananaPrice);
console.log(add + "\n");

// Subtraction
var sub = applePrice - bananaPrice;
console.log(applePrice);
console.log(applePrice - bananaPrice);
console.log(sub + "\n");

// Multiplication
var mul = applePrice * bananaPrice * pomgranatePrice;
console.log(applePrice);
console.log(applePrice * bananaPrice);
console.log(mul + "\n");

// Division
var div = applePrice / bananaPrice;
console.log(applePrice);
console.log(pomgranatePrice / applePrice);
console.log(div + "\n");

// Reminder
bananaPrice = 7;
var rem = applePrice % bananaPrice;
console.log(applePrice);
console.log(pomgranatePrice % applePrice);
console.log(rem + "\n");